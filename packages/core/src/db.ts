/**
 * Helper: Clean up the database file path string.
 */
function normalizeDatabasePath(databaseUrl: string): string {
  if (databaseUrl === ":memory:") {
    return databaseUrl;
  }

  if (databaseUrl.startsWith("sqlite://")) {
    return databaseUrl.slice("sqlite://".length);
  }

  if (databaseUrl.startsWith("file:")) {
    return databaseUrl.slice("file:".length);
  }

  return databaseUrl;
}

/**
 * Helper: Create the folder for the database file if it doesn't exist.
 */
async function ensureParentDirectory(dbPath: string) {
  if (dbPath === ":memory:") {
    return;
  }

  const parent = path.dirname(dbPath);
  await fs.mkdir(parent, { recursive: true });
}

/**
 * Problem: We need a reliable connection to the database.
 * Solution: Open the SQLite file, enable Write-Ahead Logging (WAL) for better performance,
 * and ensure Foreign Keys are enforced to keep data linked correctly.
 */
export async function openDatabase(databaseUrl: string): Promise<AppDatabase> {
  const dbPath = normalizeDatabasePath(databaseUrl);
  await ensureParentDirectory(dbPath);

  const db = await open({
    filename: dbPath,
    driver: sqlite3.Database
  });

  // Optimization: WAL mode allows reading and writing at the same time more efficiently.
  await db.exec("PRAGMA journal_mode = WAL;");
  // Performance: NORMAL sync is safe with WAL and avoids excessive fsync overhead.
  await db.exec("PRAGMA synchronous = NORMAL;");
  // Integrity: Ensure that if a session is deleted, we can't have "orphaned" messages.
  await db.exec("PRAGMA foreign_keys = ON;");

  return db;
}

/**
 * Problem: How do we create the tables the first time the app runs?
 * Solution: Run 'Migrations' (SQL scripts) that define the structure of our tables.
 */
export async function runMigrations(db: AppDatabase): Promise<void> {
  await db.exec(`
    -- Stores high-level chat sessions
    CREATE TABLE IF NOT EXISTS sessions (
      id TEXT PRIMARY KEY,
      title TEXT,
      mode TEXT NOT NULL CHECK (mode IN ('architect', 'planner', 'pitch')),
      tech_stack TEXT,
      created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
    );

    -- Stores individual messages within a session
    CREATE TABLE IF NOT EXISTS messages (
      id TEXT PRIMARY KEY,
      session_id TEXT NOT NULL,
      role TEXT NOT NULL CHECK (role IN ('user', 'assistant', 'system')),
      content TEXT NOT NULL,
      transcript_source TEXT NOT NULL CHECK (transcript_source IN ('voice', 'text')),
      created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (session_id) REFERENCES sessions(id)
    );

    -- Stores documents generated by the AI
    CREATE TABLE IF NOT EXISTS artifacts (
      id TEXT PRIMARY KEY,
      session_id TEXT NOT NULL,
      kind TEXT NOT NULL CHECK (kind IN ('architecture', 'tasks', 'pitch')),
      title TEXT NOT NULL,
      content_md TEXT NOT NULL,
      content_json TEXT NOT NULL,
      created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (session_id) REFERENCES sessions(id)
    );

    -- Tracks background jobs (like artifact generation)
    CREATE TABLE IF NOT EXISTS jobs (
      id TEXT PRIMARY KEY,
      session_id TEXT NOT NULL,
      kind TEXT NOT NULL,
      status TEXT NOT NULL CHECK (status IN ('pending', 'active', 'completed', 'failed')),
      error TEXT,
      created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
    );

    -- Indexes make searching for messages/artifacts by session ID much faster.
    CREATE INDEX IF NOT EXISTS idx_messages_session_id ON messages(session_id);
    CREATE INDEX IF NOT EXISTS idx_artifacts_session_id ON artifacts(session_id);

    -- Stores saved React Flow node positions for blueprint layout persistence
    CREATE TABLE IF NOT EXISTS blueprint_layouts (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      session_id TEXT NOT NULL,
      node_id TEXT NOT NULL,
      x REAL NOT NULL,
      y REAL NOT NULL,
      updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
      UNIQUE(session_id, node_id)
    );

    -- Stores incremental design summaries generated every N messages
    CREATE TABLE IF NOT EXISTS design_summaries (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      session_id TEXT NOT NULL,
      summary TEXT NOT NULL,
      message_count INTEGER NOT NULL,
      created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (session_id) REFERENCES sessions(id)
    );

    CREATE INDEX IF NOT EXISTS idx_blueprint_layouts_session ON blueprint_layouts(session_id);
    CREATE INDEX IF NOT EXISTS idx_design_summaries_session ON design_summaries(session_id);
  `);

  // Ensure new columns exist for existing databases (backward compatibility)
  await ensureColumnExists(db, "artifacts", "content_json", "TEXT");
  await ensureColumnExists(db, "sessions", "tech_stack", "TEXT");
}

/**
 * Helper: Safely add a column to a table if it doesn't already exist.
 */
async function ensureColumnExists(
  db: AppDatabase,
  tableName: string,
  columnName: string,
  columnDefinition: string
) {
  const rows = await db.all<{ name: string }[]>(
    `PRAGMA table_info(${tableName});`
  );

  if (rows.some((row) => row.name === columnName)) {
    return;
  }

  await db.exec(
    `ALTER TABLE ${tableName} ADD COLUMN ${columnName} ${columnDefinition};`
  );
}

/**
 * CRUD Operations (Create, Read, Update, Delete)
 */

export async function createSession(
  db: AppDatabase,
  input: CreateSessionInput
): Promise<SessionRecord> {
  await db.run(
    `INSERT INTO sessions (id, title, mode, created_at, updated_at)
     VALUES (?, ?, ?, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)`,
    input.id,
    input.title ?? null,
    input.mode
  );

  const row = await getSessionById(db, input.id);

  if (!row) {
    throw new Error("Failed to fetch created session");
  }

  return row;
}

export async function ensureSessionExists(
  db: AppDatabase,
  input: CreateSessionInput
): Promise<void> {
  const existing = await getSessionById(db, input.id);

  if (existing) {
    return;
  }

  await createSession(db, input);
}

export async function getSessionById(
  db: AppDatabase,
  sessionId: string
): Promise<SessionRecord | undefined> {
  return db.get<SessionRecord>(
    `SELECT id, title, mode, tech_stack, created_at, updated_at
     FROM sessions
     WHERE id = ?`,
    sessionId
  );
}

