export async function getSessionMessageCount(
  db: AppDatabase,
  sessionId: string
): Promise<number> {
  const row = await db.get<{ count: number }>(
    `SELECT COUNT(*) as count FROM messages WHERE session_id = ?`,
    sessionId
  );
  return row?.count ?? 0;
}

