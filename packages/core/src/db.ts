/**
 * Problem: We need to track the status of background tasks (e.g., "Is the PDF generated yet?").
 * Solution: Use an 'upsert' (Update or Insert) to track job status in the database.
 */
export async function upsertJobStatus(
  db: AppDatabase,
  input: {
    id: string;
    session_id: string;
    kind: string;
    status: "pending" | "active" | "completed" | "failed";
    error?: string | null;
  }
): Promise<void> {
  await db.run(
    `INSERT INTO jobs (id, session_id, kind, status, error, created_at, updated_at)
      VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)
      ON CONFLICT(id) DO UPDATE SET
        status = excluded.status,
        error = excluded.error,
        updated_at = CURRENT_TIMESTAMP`,
    input.id,
    input.session_id,
    input.kind,
    input.status,
    input.error ?? null
  );
}

/**
 * Blueprint Layout Persistence
 */
export async function saveNodePositions(
  db: AppDatabase,
  sessionId: string,
  positions: Array<{ id: string; x: number; y: number }>
): Promise<void> {
  for (const pos of positions) {
    await db.run(
      `INSERT INTO blueprint_layouts (session_id, node_id, x, y, updated_at)
       VALUES (?, ?, ?, ?, CURRENT_TIMESTAMP)
       ON CONFLICT(session_id, node_id) DO UPDATE SET
         x = excluded.x,
         y = excluded.y,
         updated_at = CURRENT_TIMESTAMP`,
      sessionId,
      pos.id,
      pos.x,
      pos.y
    );
  }
}

export async function getSavedNodePositions(
  db: AppDatabase,
  sessionId: string
): Promise<Array<{ id: string; x: number; y: number }>> {
  const rows = await db.all<Array<{ node_id: string; x: number; y: number }>>(
    `SELECT node_id, x, y FROM blueprint_layouts WHERE session_id = ? ORDER BY updated_at DESC`,
    sessionId
  );
  return rows.map((row) => ({ id: row.node_id, x: row.x, y: row.y }));
}

